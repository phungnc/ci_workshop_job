<?php 
class IntToStr {
	static function intToStrBetween1_7($val) {
		if ($val === 1) {
			return "1";
		} else if ($val === 2) {
			return "2";
		} else if ($val === 3) {
			return "3";
		} else if ($val === 4) {
			return "4";
		} else if ($val === 5) {
			return "5";
		} else if ($val === 6) {
			return "6";
		} else if ($val === 7) {
			return "7";
		}
	}
}